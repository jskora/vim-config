#!/bin/bash
#------------------------------------------------------------
# Install Vim RC file
#------------------------------------------------------------

safe_copy () {
    SRC=$1
    DST=${HOME}/.$1
    if [ -f ${DST} ]; then
	cp -p ${DST} ${DST}.orig
	echo "backup made ${DST} => ${DST}.orig"
    fi
    cp ${SRC} ${DST}
    echo "installed ${SRC} as new ${DST}"
}

safe_copy vimrc

#--------------------------------------------------

echo "$0 done"

#============================================================
# done
#============================================================
